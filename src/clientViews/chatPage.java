     private void listerDestinataire(String utilisateur) {
         try{
           reponse=clientController.listerAmis(utilisateur);
             String[] requete = reponse.split(",");
                if(requete[2].equals("succes")){
                    int size=requete.length;
                     if(size>3){
                        String [] listAmis=requete[3].split("\n");
                        for (String ami : listAmis) {
                              destinataire.addItem(ami);
                        }
                     }
                   
                }   
         }catch(Exception e){
              System.out.println("Exception Lister Amis" + e.getMessage());
         }
     }
