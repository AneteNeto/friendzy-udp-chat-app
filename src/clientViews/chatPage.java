    private void envoieButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        
      try{
            if(sujet.getText().isEmpty() || message.getText().isEmpty()){
             JOptionPane.showMessageDialog(this, "Il faut saisir tous les champs", "Validation Champs", JOptionPane.ERROR_MESSAGE);
            }else{
                if(destinataire.getSelectedIndex()!=0){
                    String dest=destinataire.getSelectedItem().toString();
                    reponse=clientController.envoyerMessage(currentUtilisateur,dest,sujet.getText(),message.getText());
                }else{
                    reponse=clientController.envoyerMessageBroadcast(currentUtilisateur,sujet.getText(),message.getText());
                }
                String[] requete = reponse.split(",");
                if(requete[2].equals("succes")){
                       JOptionPane.showMessageDialog(this, "Message Envoyéé", "Message", JOptionPane.INFORMATION_MESSAGE);
                       sujet.setText("");
                       message.setText("");
                }

            }
        }catch(Exception ex){
             ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

